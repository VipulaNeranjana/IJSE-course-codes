public class TypeSystem {
    public static void main(String[] args) {
        //literals of boolean

        System.out.println(true);
        System.out.println(false);

        //literals of numbers

        System.out.println(0b111);  //binary
        System.out.println(0B111);

        System.out.println(010);    //octal

        System.out.println(7);      //decimal

        System.out.println(0x24b);  //hexadecimal
        System.out.println(0X24b);

        System.out.println(12.25e2); //12.25*10^2
        System.out.println(12.25E2);
        System.out.println(12.25E-2);//12.25*10^-2

        //literals of characters
        System.out.println('a');
        System.out.println("this is a string");

        System.out.println(0x1p-2);
        System.out.println(211545525478L);
    }
}
